#include "ft_printf.h"

size_t	ft_wide_size(unsigned int wide)
{
	size_t	i;

	i = 0;
	while (!(wide & 2147483648) && i < 32)
	{
		wide = wide << 1;
		i++;
	}
	return (32 - i);
}
